#This module provides access to the mathematical functions defined by the C standard.
import math

pi = 3.14
x = 1
y = 2
z = 3
#round function
print(round(pi))
#ceiling function
print(math.ceil(pi))
#floor function
print(math.floor(pi))
#absolute value function
print(abs(pi))
#power function
print(pow(pi, 2))
#square root function
print(math.sqrt(pi))
#max function: find the greatest number
print(max(pi, x, y, z))
#min function: find the least number
print(min(pi, x, y, z))